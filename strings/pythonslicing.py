# it gives us a range of characters by using the slice syntax

x = "Hello, World"
print(x[2:5])
# llo

# Sliceing from the start
b = "Hello, World"
print(b[:5])
# Hello

# Slicing fro the end
c = "Hello, World"
print(c[2:])
# llo, World

# Negative Indexing
# Keep this in mind
# Positive Indexing = Left -> Right
# Negativing Indexing = Right -> Left

y = "Shivam"
print(y[-1:-4:-1])

z = "Hello, World!"
print(z[-5:-2])

x = 'Welcome'
print(x[3:5])